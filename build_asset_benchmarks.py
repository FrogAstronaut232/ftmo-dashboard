"""Build per-asset buy-and-hold benchmark overlays for the live equity charts.

For each (account, strategy) we write a `benchmark.csv` next to the strategy's
`live/equity.csv`. Each column is one traded FX pair = the account's starting
capital ($50k / $200k) invested in that single pair on the strategy's start
date and held (no leverage, no trading). The dashboard draws one faint dashed
line per column so you can see how much edge the strategy added over simply
holding each underlying.

Why a SEPARATE file (not a column in equity.csv): the live equity.csv files
are regenerated by the daily report.py pipeline, which would clobber any extra
columns. benchmark.csv is owned solely by this script.

Windows is currently date 2026-06; FX bars come from yfinance ("<PAIR>=X").

Run:
  py website\build_asset_benchmarks.py
"""
from __future__ import annotations

import sys
from pathlib import Path

import pandas as pd
import yfinance as yf

HERE = Path(__file__).resolve().parent          # website/
DATA = HERE / "data"

# Traded universe per strategy (must match the live execution config).
PAIRS = {
    "g2":  ["EURUSD", "GBPJPY"],
    "g10": ["AUDJPY", "AUDUSD", "EURGBP", "EURUSD", "GBPJPY",
            "GBPUSD", "NZDUSD", "USDCAD", "USDCHF", "USDJPY"],
}

INITIAL = {"trial": 50_000.0}

# Single live account: the $50k FTMO Free Trial running G10 only. Its chart
# extends the strategy line to "today", so the benchmark does too (nothing is
# archived/frozen anymore).
ARCHIVED = set()

ACCOUNTS = ["trial"]
STRATS = ["g10"]


def _fetch_close(pair: str, start: str, end: str) -> pd.Series | None:
    """Daily close for <PAIR>=X over [start, end), indexed by date."""
    df = yf.download(f"{pair}=X", start=start, end=end, progress=False,
                     auto_adjust=False, multi_level_index=False)
    if df is None or df.empty:
        return None
    close = df["Close"].astype(float)
    close = close[~close.index.duplicated(keep="first")]
    close.index = pd.to_datetime(close.index).normalize()
    return close


def build(acct: str, strat: str) -> None:
    eq_path = DATA / acct / strat / "live" / "equity.csv"
    if not eq_path.exists():
        print(f"  skip {acct}/{strat}: no equity.csv")
        return

    eq = pd.read_csv(eq_path, parse_dates=["date"])
    if eq.empty:
        print(f"  skip {acct}/{strat}: empty equity.csv")
        return

    start = eq["date"].min().normalize()
    end_eq = eq["date"].max().normalize()
    today = pd.Timestamp.utcnow().tz_localize(None).normalize()
    end = end_eq if acct in ARCHIVED else max(end_eq, today)

    initial = INITIAL[acct]
    pairs = PAIRS[strat]

    # Fetch a little past `end` so the final day is included by yfinance's
    # half-open [start, end) convention.
    fetch_start = start.strftime("%Y-%m-%d")
    fetch_end = (end + pd.Timedelta(days=3)).strftime("%Y-%m-%d")

    closes: dict[str, pd.Series] = {}
    for p in pairs:
        c = _fetch_close(p, fetch_start, fetch_end)
        if c is None or c.empty:
            print(f"    WARN {acct}/{strat}: no bars for {p}")
            continue
        closes[p] = c

    if not closes:
        print(f"  skip {acct}/{strat}: no pair data fetched")
        return

    bench = pd.DataFrame(closes).sort_index()
    bench = bench[(bench.index >= start) & (bench.index <= end)]
    bench = bench.ffill().bfill()

    # Normalise each pair to `initial` at the first in-window observation so all
    # lines start at the account's starting capital, exactly like the strategy.
    norm = bench.copy()
    for col in norm.columns:
        series = norm[col].dropna()
        if series.empty:
            continue
        base = series.iloc[0]
        norm[col] = (norm[col] / base * initial).round(2)

    # Preserve the configured pair order for stable line colours on the page.
    ordered_cols = [p for p in pairs if p in norm.columns]
    norm = norm[ordered_cols]

    out = norm.reset_index()
    out = out.rename(columns={out.columns[0]: "date"})
    out["date"] = pd.to_datetime(out["date"]).dt.strftime("%Y-%m-%d")

    out_path = eq_path.parent / "benchmark.csv"
    out.to_csv(out_path, index=False)

    finals = {c: f"${out[c].iloc[-1]:,.0f}" for c in ordered_cols}
    print(f"  wrote {out_path.relative_to(HERE)}  "
          f"({len(out)} rows {out['date'].iloc[0]} -> {out['date'].iloc[-1]}, "
          f"{len(ordered_cols)} pairs)")
    print(f"    final B&H: {finals}")


def main() -> int:
    print("Building per-asset buy-and-hold benchmark overlays...")
    for acct in ACCOUNTS:
        for strat in STRATS:
            print(f"\n=== {acct} / {strat} ===")
            try:
                build(acct, strat)
            except Exception as e:  # noqa: BLE001 - report and continue
                print(f"  ERROR {acct}/{strat}: {e!r}")
    print("\nDone.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
